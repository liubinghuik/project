package com.accp.domain;

import java.util.Date;

public class Recharge {
    private Integer reid;

    private Integer mid;

    private Float remoney;

    private String reway;

    private Integer rejf;

    private Float rebalance;

    private String remark;

    private Date createtime;

    public Integer getReid() {
        return reid;
    }

    public void setReid(Integer reid) {
        this.reid = reid;
    }

    public Integer getMid() {
        return mid;
    }

    public void setMid(Integer mid) {
        this.mid = mid;
    }

    public Float getRemoney() {
        return remoney;
    }

    public void setRemoney(Float remoney) {
        this.remoney = remoney;
    }

    public String getReway() {
        return reway;
    }

    public void setReway(String reway) {
        this.reway = reway;
    }

    public Integer getRejf() {
        return rejf;
    }

    public void setRejf(Integer rejf) {
        this.rejf = rejf;
    }

    public Float getRebalance() {
        return rebalance;
    }

    public void setRebalance(Float rebalance) {
        this.rebalance = rebalance;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public Date getCreatetime() {
        return createtime;
    }

    public void setCreatetime(Date createtime) {
        this.createtime = createtime;
    }
}